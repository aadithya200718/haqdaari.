function buildScheme(raw) {
  return {
    schemeId: raw.id,
    schemeName: raw.name,
    schemeNameHindi: raw.hindi,
    authority: raw.auth,
    benefit: {
      amount: raw.amt,
      frequency: raw.freq,
      currency: "INR",
      description: raw.desc,
    },
    eligibility: raw.elig,
    requiredDocuments: raw.docs,
    applicationProcess: raw.appProcess || "Apply online or at nearest CSC",
    contactInfo: raw.help,
    sourceUrl: raw.src || "https://www.myscheme.gov.in",
    status: "active",
  };
}

const schemes = [];

// 1. Central government schemes
for (const c of CENTRAL) {
  schemes.push(buildScheme(c));
}

// 2. Real state schemes
for (const s of STATE_SCHEMES) {
  schemes.push(buildScheme(s));
}

const output = JSON.stringify(schemes, null, 2);
fs.writeFileSync(__dirname + "/schemes/all-schemes.json", output, "utf8");
console.log(
  `Generated ${schemes.length} schemes (${CENTRAL.length} central + ${STATE_SCHEMES.length} state)`,
);
console.log(
  "Data sources: myscheme.gov.in, state government portals, PIB press releases",
);
